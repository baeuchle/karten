function sind(val) {
  return Math.sin(val * Math.PI / 180);
}
function cosd(val) {
  return Math.cos(val * Math.PI / 180);
}
function tand(val) {
  return Math.tan(val * Math.PI / 180);
}
function asind(val) {
  return 180 / Math.PI * Math.asin(val);
}
function acosd(val) {
  return 180 / Math.PI * Math.acos(val);
}
function atand(val) {
  return 180 / Math.PI * Math.atan(val);
}
function atan2d(y, x) {
  return 180 / Math.PI * Math.atan2(y, x);
}
